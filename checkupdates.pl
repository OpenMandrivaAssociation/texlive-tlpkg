foreach my $item (@dirlist) {
    unless (-d "../$item") {
	if ($options{'i'}) {
	    `cd ..; abf get $item`;
	}
	else {
	    print("$item: not checked out\n");
	}
    }
}

foreach my $spec (glob("../texlive-*/*.spec")) {
    open(IN, "<$spec");
    my $line = <IN>;
    if ($line =~ /# revision (\d+)/) {
	my $revision = $1;
	$spec =~ m/\.\.\/texlive-(.*)\//;
	my $name = $1;
	unless ($namerev{$name} || grep($_ eq "texlive-$name", @dirlist)) {
	    print("texlive-$name: not handled\n");
	}
	$namerev{$name} = $revision;
    }
    close(IN);
}

sub should_skip {
    my ($package) = @_;
    return 1 if (!$enable_asymptote && $package =~ /^asymptote/);
    return 1 if (!$enable_xindy && $package =~ /^xindy/);
    return 1 if ($with_system_lcdf && $package =~ /^lcdftypetools/);
    return 1 if ($with_system_psutils &&
		 ($package =~ /^psutils/ ||
		  $package =~ /^getafm/));
    return 1 if ($with_system_t1lib &&
		 $package =~ /^t1utils/);
    return 1 if ($package =~ /texworks/ ||
		 $package eq "texlive-msg-translations" ||
		 $package =~ /wintools/);
    return 1 if ($package =~ m/00texlive\./ ||
		 $package =~ m/texlive.infra/);
    return 0;
}

# Check only noarch packages
foreach my $package ($tlpdb->list_packages()) {
    next if should_skip($package);
    if ($package =~ /(.*)\.(.*)/) {
	my $platform = $2;
	if ($platform eq $arch) {
	    next;
        }
	else {
	    next if ($platform =~ /(armel|armhf|alpha|i386|mipsel|powerpc|sparc|x86_64)-linux/ ||
		     $platform =~ /(amd64|i386)-k?freebsd/ ||
		     $platform =~ /i386-cygwin/ ||
		     $platform =~ /(i386|amd64)-netbsd/ ||
		     $platform =~ /mips-irix/ ||
		     $platform =~ /powerpc-aix/ ||
		     $platform =~ /(i386|sparc|x86_64)-solaris/ ||
		     $platform =~ /(x86_64|universal)-darwin/ ||
		     $platform =~ /win32/);
	}
    }
    my $tlpobj = $tlpdb->get_package($package);
    my $revision = $tlpobj->{'revision'};
    $revision = "undef" unless ($revision);
    if (!defined($namerev{$package})) {
	print("texlive-$package: not packaged ($revision), required by ");
	my @requires = $tlpdb->needed_by($package);
	if ($#requires >= 0) {
	    print(join(" ", @requires));
	}
	else {
	    print("<<nothing>>");
	}
	print("\n");
	next;
    }
    if ($namerev{$package} ne $revision) {
	if ($options{'i'}) {
	    `cd ../texlive-$package; git pull`;
	    open(IN, "../texlive-$package/texlive-$package.spec");
	    my $line = <IN>;
	    close(IN);
	    if ($line =~ /# revision (\d+)/) {
		next if ($1 eq $revision);
	    }
	}
	print("texlive-$package: revision changed ($namerev{$package} -> $revision)\n");
    }
}

# Upstream may have deprecrated package
