if ($#ARGV >= 0 or $error or $options{'h'} or $options{'?'}) {
    print("$0 [-ih?]\n",
	  "-u		Update texlive.tlpdb\n",
	  "-i		Initialize (checkout missing packages).\n",
	  "-h, -?	Prints this help message and exists.\n");
    exit(0);
}

if ($options{'u'}) {
    `wget http://mirrors.ctan.org/systems/texlive/tlnet/tlpkg/texlive.tlpdb.xz`;
